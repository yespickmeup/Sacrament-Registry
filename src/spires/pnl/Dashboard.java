    private void login() {
        String user_name = tf_fname.getText();
        String password = jPasswordField1.getText();
        String where = " where user_name='" + user_name + "' and password='" + password + "' limit 1";
        List<Users.to_users> datas = Users.ret_data(where);
        if (datas.isEmpty()) {
            Alert.set(0, "Please input correct credentials!");
            tf_fname.setText("");
            jPasswordField1.setText("");
            tf_fname.grabFocus();

        } else {
            layout.show(pnl_holder, "1");
            Users.to_users user = datas.get(0);
            Res.setUser_id("" + user.id);
            Res.setUser_name("" + user.user_name);
            Res.setUser_screename(user.user_code);
            tf_fname.setText("");
            jPasswordField1.setText("");
            set_privileges(user);
        }
    }

